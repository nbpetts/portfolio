                    <p>I designed the lightweight pilot study that would offer a large return on effort. I wrote an interview guide, recruited participants and conducted twelve half-hour phone interviews. Then I compiled the notes of any observers and transcribed chunks of text from audio recordings. I put these notes and chunks of text into an affinity diagram for analysis. </p>                   
                </div>


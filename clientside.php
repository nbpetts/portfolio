                    <p>After some discussion with my mentors, we decided to focus on five themes that Basecamp could conceivably help with and return to them with more in-depth research. I reported our findings to the company.</p> 
                    <p>We also decided to do a few verification interviews to make sure the five identified themes were on target. I recruited a few more participants and asked some more focused questions and found that the themes were known to the verification participants. </p>
                    <h2>Contextual Inquiry </h2>
                    <p>Now that I understood some of the basic tenets of client work, I wanted to dive deeper into how Basecamp users actually accomplish their client work. I knew that on-site observations would allow me to view  how users actually used the product. I created an Observation Guide of seven tasks and a short interview. The tasks were focused on the five themes I discovered from the pilot study.</p>

                </div>


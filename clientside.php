                    <p>Since these sessions were more personal and more of a time commitment, recruitment was more difficult this time. But after several rounds of emails and message revision, I found six participants who would let me come to their workplace and observe what they do.</p> 
                    <p>The observation sessions were really interesting and helped me understand the specific tasks users do with their clients. I recorded audio, took still pictures and wrote notes of each session. I compiled the notes and images into writeups so my mentors could follow along.</p>
                    <p>Next I did the analysis. I used an affinity diagram again to evaluate notes and images from the sessions. Clearer themes emerged from this diagram, though we chose to focus on three core themes that we thought were the most important for Basecamp users. I reported these themes to the team, showing them useful quotes and helping them gain insight into the specifics of client work. This insight should help them design a more successful product. </p>

